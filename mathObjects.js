// Math.round
console.log(Math.round(356.98));
console.log(Math.round(2.4));

// Math.ceil
console.log(Math.ceil(2.1));
console.log(Math.ceil(-2.1));

// floor
console.log(Math.floor(29.6));

// trunc
console.log(Math.trunc(29.8));

// sign
console.log(Math.sign(-24));
console.log(Math.sign(24));

// power
console.log(Math.pow(3,2));

// squre root
console.log(Math.sqrt(65).toPrecision(4));

// pi
console.log(Math.PI);

// max min
console.log(Math.min(2,4,50,1,3));
console.log(Math.max(2,4,1,30,50,38,51));

// random
console.log((Math.random()*10).toPrecision(3));
console.log(Math.floor(Math.random()*10));
console.log(Math.floor(Math.random()*100));
